    user_id    bigint not null
        constraint fknvx9seeqqyy71bij291pwiwrg
            references users,
    constraint uk2jovqhqo324cubdomovkex03b
        unique (user_id, post_id)
);

create table comments
(
    id         bigint generated by default as identity
